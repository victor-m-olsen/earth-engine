var add_cloud_percentage = function(img){
  
  var cloudmask = img.select('cloudmask')
  var cloud_area = cloudmask.eq(1).multiply(ee.Image.pixelArea()).rename('cloud_area')//.divide(1000000)
  var total_area = cloudmask.eq(1).or(cloudmask.eq(0)).multiply(ee.Image.pixelArea()).rename('total_area')//.divide(1000000)
  
  var area = cloud_area.addBands(total_area).reduceRegion({
    geometry: aoi,
    reducer: ee.Reducer.sum(),
    scale: 10,
    maxPixels: 20000000000,
    tileScale: 16
  })
  
  var perc = ee.Number(area.get('cloud_area')).divide(ee.Number(area.get('total_area')))
  
  return img.set('cloud_perc', perc)
}

